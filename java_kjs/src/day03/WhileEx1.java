package day03;

public class WhileEx1 {
	// while문 예제
	public static void main(String[] args) {
		// Hello world를 5번 출력하시오
		System.out.println("Hello world!!");
		System.out.println("Hello world!!");
		System.out.println("Hello world!!");
		System.out.println("Hello world!!");
		System.out.println("Hello world!!");

		System.out.println("================================================");
		
		int num = 0;
		// int count = 5;
		while(num < 5/*count로 대체가능*/ ) {

			System.out.println("Hello world!!");
			
			num++;
		}
		
		
	}

}
