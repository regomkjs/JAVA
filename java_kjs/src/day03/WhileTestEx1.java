package day03;

public class WhileTestEx1 {

	public static void main(String[] args) {
		// 1부터 5까지 출력되는 코드를 작성하세요
		
		/* 반복회수	: 1부터 5까지 1씩 증가
		 * 규칙성		: 콘솔에 출력
		 * 반복문종료후	: 없음
		 */
		int num = 1;
		
		while (num <= 5) {
			
			System.out.println(num);
			
			num++;
		}
		
		System.out.println("==============================");
		
		int num2 = 0;
		
		while (num2 < 5) {
			++num2;
			System.out.println(num2);
			
		}
		
	
	
	
	
	}

}
